# Get two numbers from the user
num1 = float(5)
num2 = float(2)


# Perform operations
print(f"Addition: {num1 + num2}")
print(f"Subtraction: {num1 - num2}")
print(f"Multiplication: {num1 * num2}")

if num2 != 0:
    print(f"Division: {num1 / num2}")
else:
    print("Division: Undefined (cannot divide by zero)")
